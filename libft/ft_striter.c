#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t x;

	if (s == NULL || f == NULL)
		return ;
	x = 0;
	while (s[x])
	{
		f(&s[x]);
		x++;
	}
	return ;
}
